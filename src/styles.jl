function get_way_style(tags::Dict, cascade::Array{Any})
		println()
		println()
		println()
		println("<----------NEW STYLE---------->")
		if cascade != []
			style = nothing
			for rule in cascade
				d = rule[2]
				if d == Dict()
					continue
				end
				for i in keys(d)
					if haskey(tags, i)
						if d[i][1] == "yes"
							if style == nothing
								style = rule[end]
							else
								style = merge(style, rule[end])
							end
							println("$(i) -- true")
							continue
						end
						operator = d[i][2]
						if operator == "="
							operator = "=="
						end
						if tryparse(Float64, tags[i]) != nothing && tryparse(Float64, d[i][1]) !=nothing
							eval_statement = "$(tags[i])$(operator)$(d[i][1])"
						else
							eval_statement = "\"$(tags[i])\"$(operator)\"$(d[i][1])\""
						end
						println("$(eval_statement) -- $(eval(Meta.parse(eval_statement)))")
						if eval(Meta.parse(eval_statement)) == true
							if style == nothing
								style = rule[end]
							else
								style = merge(style, rule[end])
							end
						end
					end
				end
			end
			show(style)
			for i in keys(tags)
				if occursin(i, "name")
					println(tags[i])
				end
			end
			return style
		end
        for tag in ["waterway", "building", "amenity", "highway", "leisure", "nature","manmade"]
            if haskey(tags, tag)
                if haskey(tag2style[tag], tags[tag])
                    return tag2style[tag][tags[tag]]
                end
            end
        end
        return Dict("color" => 0xD3D3D3, "width"=>  1, "spec"=>  "-", "is_polygon" =>  false)
    end
